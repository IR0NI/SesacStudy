	// 제거할 수 있다.
	// new와 delete는 연산자이다.
	// new를 이용해서 생성한 메모리를 delete를 이용해서 제거하지 않으면
	// 메모리 릭이 발생하게 된다.
	// new 변수타입; 을 해주면 변수타입의 크기만큼 힙에 메모리를 생성하고
	// 그 주소를 반환해준다.
	// delete 주소; 를 해주면 해당 주소를 메모리 해제해준다. 반드시 동적할
	// 당된 주소만 넣어주어야 한다.
